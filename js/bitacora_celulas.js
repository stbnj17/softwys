		    var nom_lider = $("#nom_lider" + id).val();
		    var anfitrion = $("#anfitrion" + id).val();
		    var grupo = $("#grupo" + id).val();
		    var estad = $("#estad" + id).val();
		   
		    $("#nombre2").val(nombre);
		    $("#sector2").val(sector);
		    $("#id_miembro3").val(supervisor);
		    $("#id_miembro4").val(lider);
		    $("#nombre_miembro3").val(nom_supervisor);
		    $("#nombre_miembro4").val(nom_lider);
		    $("#anfi2").val(anfitrion);
		    $("#grupo2").val(grupo);
		    $("#estado2").val(estad);
